function convertToRoman(num) {
  let s=''
  while(num>=1000){
    num-=1000
    s+='M'
  }
  while(num>=900){
    num-=900
    s+='CM'
  }
  while(num>=500){
    num-=500
    s+='D'
  }
  while(num>=400){
    num-=400
    s+='CD'
  }
  while(num>=100){
    num-=100
    s+='C'
  }
  while(num>=90){
    num-=90
    s+='XC'
  }
  while(num>=50){
    num-=50
    s+='L'
  }
  while(num>=40){
    num-=40
    s+='XL'
  }
  while(num>=10){
    num-=10
    s+='X'
  }
  while(num>=9){
    num-=9
    s+='IX'
  }
  while(num>=5){
    num-=5
    s+='V'
  }
  while(num>=4){
    num-=4
    s+='IV'
  }
  while(num>=1){
    num-=1
    s+='I'
  }
 return s;
}

convertToRoman(36);
